class Solution
{
    public:
    //Function to find the first non-repeating character in a string.
    char nonrepeatingCharacter(string S)
    {
       vector<int> visited(26);
       char ans = '$';
       
       for(int i = S.size()-1;  i>=0; i--) {
            visited[S[i]-'a']++;
       }
       
       for(auto it : S) {
           if(visited[it-'a'] == 1) return it;
       }
       
       return '$';
    }

};

